// Switch Case use this in multiple conditions
/* Switch Case Structure
 switch (key){
    case value:
        break;
    default:
        break;
 }
*/

const Month=3 // this is key
switch(Month){
 case 1: // this is value
    console.log("January")
 break;
 case 2:
    console.log("Feb")
 break;
 case 3:
    console.log("March")
 break;
 case 4:
    console.log("April")
 break;
 default:
 console.log("Default")
 break
}